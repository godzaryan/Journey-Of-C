/*
    This program demonstrates the use of floating/real_valued data type
    eg of float are(-1.355,-55.215,0.333,5.258,9.1,11.11,etc)
*/
#include<stdio.h>

int main()
{
    float y; // floating variable is created with the keyword float
    // Now y variable can hold the real values/ decimal values
    y=1.25; // can hold the value 1.25

    // lets display the value of y
    printf("%f",y); // here, %f denotes that y is a floating variable
    // output will be 1.25 as mentioned
    return 0;
}